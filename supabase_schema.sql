create or replace function public.generate_report_code()
returns text
language plpgsql
as $$
declare
  candidate text;
begin
  loop
    candidate := lpad(floor(random() * 1000000)::text, 6, '0');
    exit when not exists (
      select 1
      from public.bejelentesek b
      where b.report_code = candidate
    );
  end loop;

  return candidate;
end;
$$;

create or replace function public.set_report_code_before_insert()
returns trigger
language plpgsql
as $$
begin
  if new.report_code is null or btrim(new.report_code) = '' then
    new.report_code := public.generate_report_code();
  end if;

  return new;
end;
$$;

drop trigger if exists trg_set_report_code_before_insert on public.bejelentesek;
create trigger trg_set_report_code_before_insert
before insert on public.bejelentesek
for each row
execute function public.set_report_code_before_insert();

update public.bejelentesek
set report_code = public.generate_report_code()
where report_code is null or btrim(report_code) = '';

