create or replace function public.enforce_simple_rate_limit()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  hourly_count integer;
  daily_count integer;
begin
  if auth.uid() is null then
    raise exception 'Bejelentkezés szükséges.';
  end if;

  if TG_TABLE_NAME = 'bejelentesek' then
    select count(*)
      into hourly_count
      from public.bejelentesek b
      where b.user_id = auth.uid()
        and b.created_at > now() - interval '1 hour';
    if hourly_count >= 5 then
      raise exception 'Túl sok bejelentés az elmúlt 1 órában (max: 5).';
    end if;

    select count(*)
      into daily_count
      from public.bejelentesek b
      where b.user_id = auth.uid()
        and b.created_at > now() - interval '24 hours';
    if daily_count >= 20 then
      raise exception 'Túl sok bejelentés az elmúlt 24 órában (max: 20).';
    end if;
  elsif TG_TABLE_NAME = 'uzenetek' then
    select count(*)
      into hourly_count
      from public.uzenetek u
      where u.from_user_id = auth.uid()
        and u.created_at > now() - interval '1 hour';
    if hourly_count >= 60 then
      raise exception 'Túl sok üzenet az elmúlt 1 órában (max: 60).';
    end if;
  elsif TG_TABLE_NAME = 'abuse_reports' then
    select count(*)
      into hourly_count
      from public.abuse_reports a
      where a.reporter_user_id = auth.uid()
        and a.created_at > now() - interval '1 hour';
    if hourly_count >= 20 then
      raise exception 'Túl sok jelentés az elmúlt 1 órában (max: 20).';
    end if;
  end if;

  return NEW;
end;
$$;

drop trigger if exists trg_rate_limit_bejelentesek on public.bejelentesek;
create trigger trg_rate_limit_bejelentesek
before insert on public.bejelentesek
for each row
execute function public.enforce_simple_rate_limit();

